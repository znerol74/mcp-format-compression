#!/usr/bin/env python3
"""Rename weekend BenchPP logs to the deterministic name format so new resubmits resume.

Old format (auto-generated by run.py when --log_file not set):
  {input_stem}_{fmt}_{model_hf_slashesReplaced}_openwebui_{YYYYMMDD_HHMMSS}.json

New format (we now pass --log_file explicitly):
  {input_stem}_{fmt}_tc{tc_fmt}_{model_tag}_resume.json

Mapping rule — the weekend slurm ran VARIANTS in this order:
    json:json, toon:json, tron:json, toon:toon, tron:tron
So within a category dir, files sorted by timestamp map to:
    1st json file → json_tcjson
    1st toon file → toon_tcjson
    2nd toon file → toon_tctoon
    1st tron file → tron_tcjson
    2nd tron file → tron_tctron
"""
import os
import re
import sys
from pathlib import Path
from collections import defaultdict

LOGS_ROOT = Path.home() / "phd" / "MCPToolBenchPP" / "logs"

# Map HF model name (as appearing in filename, with / → _) to MODEL_TAG
HF_TO_TAG = {
    "stelterlab_Mistral-Small-24B-Instruct-2501-AWQ": "mistral-small-24b",
    "Qwen_Qwen3-32B-AWQ":                              "qwen3-32b-awq",
    "Qwen_Qwen3-32B":                                  "qwen3-32b-fp16",
    "casperhansen_deepseek-r1-distill-qwen-32b-awq":   "deepseek-r1-32b",
}

# Variant occurrence order within a category (for assigning the Nth file of fmt=X)
OCCURRENCE_TO_TCFMT = {
    "json": ["json"],            # only 1 json variant
    "toon": ["json", "toon"],    # 1st toon file = toon:json (exp1), 2nd = toon:toon (exp2)
    "tron": ["json", "tron"],    # same
}

# Parse: {input_stem}_{fmt}_{hf_slashes_replaced}-openwebui_{timestamp}.json
FILE_RE = re.compile(
    r"^(?P<stem>.+?)_(?P<fmt>json|toon|tron)_"
    r"(?P<hf>.+?)-openwebui_(?P<ts>\d{8}_\d{6})\.json$"
)


def main():
    dry_run = "--dry-run" in sys.argv
    if not LOGS_ROOT.is_dir():
        sys.exit(f"ERROR: {LOGS_ROOT} does not exist")

    for cat_dir in sorted(LOGS_ROOT.iterdir()):
        if not cat_dir.is_dir():
            continue

        # Group files by (model_tag, fmt), sorted by timestamp
        by_key = defaultdict(list)
        for f in cat_dir.iterdir():
            m = FILE_RE.match(f.name)
            if not m:
                print(f"  skip (pattern mismatch): {f.name}")
                continue
            model_tag = HF_TO_TAG.get(m["hf"])
            if model_tag is None:
                print(f"  skip (unknown HF model '{m['hf']}'): {f.name}")
                continue
            by_key[(model_tag, m["fmt"])].append((m["ts"], m["stem"], f))

        # Assign each timestamp-sorted occurrence to a tc_fmt
        for (model_tag, fmt), entries in by_key.items():
            entries.sort()
            tcfmts = OCCURRENCE_TO_TCFMT[fmt]
            for i, (ts, stem, src) in enumerate(entries):
                if i >= len(tcfmts):
                    print(f"  skip (extra file beyond expected variants): {src.name}")
                    continue
                tc_fmt = tcfmts[i]
                new_name = f"{stem}_{fmt}_tc{tc_fmt}_{model_tag}_resume.json"
                dst = src.parent / new_name
                if dst.exists():
                    print(f"  skip (dst exists): {dst.name}")
                    continue
                action = "DRY" if dry_run else "mv "
                print(f"  {action} {src.name}")
                print(f"      → {new_name}")
                if not dry_run:
                    src.rename(dst)


if __name__ == "__main__":
    main()
